const fs = require("fs");
const path = require("path");
const hre = require("hardhat");

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function loadDeployment(networkName) {
  const deploymentFile = path.join(__dirname, "..", "deployments", `${networkName}.json`);
  if (!fs.existsSync(deploymentFile)) {
    throw new Error(`Deployment file not found: ${deploymentFile}`);
  }
  return readJson(deploymentFile);
}

function loadAbi(contractName) {
  const candidates = [
    path.join(
      __dirname,
      "..",
      "artifacts",
      "contracts",
      `${contractName}.sol`,
      `${contractName}.json`
    ),
    path.join(
      __dirname,
      "..",
      "artifacts",
      "contracts",
      "contracts",
      `${contractName}.sol`,
      `${contractName}.json`
    )
  ];

  const artifactPath = candidates.find((candidate) => fs.existsSync(candidate));
  if (!artifactPath) {
    throw new Error(`Artifact not found for ${contractName}. Checked: ${candidates.join(", ")}`);
  }

  return readJson(artifactPath).abi;
}

function updateFrontendEnvironment(addresses) {
  const envPath = path.join(__dirname, "..", "..", "frontend", "src", "environments", "environment.ts");
  if (!fs.existsSync(envPath)) {
    throw new Error(`Frontend environment not found: ${envPath}`);
  }

  const current = fs.readFileSync(envPath, "utf8");
  const next = current.replace(
    /contracts:\s*\{[\s\S]*?\}\s*,\s*ipfsGateway:/m,
    `contracts: {\n    agentNFT: '${addresses.AgentNFT}',\n    forgeToken: '${addresses.ForgeToken}',\n    erc6551Registry: '${addresses.ERC6551Registry}',\n    jobEscrow: '${addresses.JobEscrow}',\n    rentalMarket: '${addresses.RentalMarket}'\n  },\n  ipfsGateway:`
  );

  fs.writeFileSync(envPath, next);
}

async function main() {
  const networkName = hre.network.name;
  const deployment = loadDeployment(networkName);
  const addresses = deployment.contracts;

  const contractsForFrontend = {
    forgeToken: {
      address: addresses.ForgeToken,
      abi: loadAbi("ForgeToken")
    },
    agentNFT: {
      address: addresses.AgentNFT,
      abi: loadAbi("AgentNFT")
    },
    erc6551Registry: {
      address: addresses.ERC6551Registry,
      abi: loadAbi("ERC6551Registry")
    },
    erc6551AccountImplementation: {
      address: addresses.ERC6551AccountImplementation,
      abi: loadAbi("ERC6551Account")
    },
    jobEscrow: {
      address: addresses.JobEscrow,
      abi: loadAbi("JobEscrow")
    },
    rentalMarket: {
      address: addresses.RentalMarket,
      abi: loadAbi("RentalMarket")
    }
  };

  const frontendContractsDir = path.join(__dirname, "..", "..", "frontend", "src", "app", "core", "contracts");
  fs.mkdirSync(frontendContractsDir, { recursive: true });

  const generatedPath = path.join(frontendContractsDir, "generated-contracts.ts");
  const generatedContent =
    "// Auto-generated by contracts/scripts/export-frontend.js\n" +
    `export const DEPLOYED_NETWORK = '${networkName}' as const;\n` +
    `export const CONTRACTS = ${JSON.stringify(contractsForFrontend, null, 2)} as const;\\n`;

  fs.writeFileSync(generatedPath, generatedContent);
  updateFrontendEnvironment(addresses);

  console.log(`Exported frontend contracts to: ${generatedPath}`);
  console.log("Updated frontend environment addresses.");
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
